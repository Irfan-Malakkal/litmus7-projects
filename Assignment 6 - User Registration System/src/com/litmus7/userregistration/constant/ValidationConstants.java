package com.litmus7.userregistration.constant;

/**
 * Defines constant values used for user input validation constraints.
 * 
 * <p>This class provides fixed minimum and maximum values for user age,
 * and minimum length requirements for passwords.</p>
 */
public class ValidationConstants {
    /**
     * The minimum age allowed for user registration.
     */
    public static final int MIN_AGE = 18;
    /**
     * The maximum age allowed for user registration.
     */
    public static final int MAX_AGE = 60;
    /**
     * The minimum number of characters required for a valid password.
     */
    public static final int MIN_PASSWORD_LENGTH = 6;
}
